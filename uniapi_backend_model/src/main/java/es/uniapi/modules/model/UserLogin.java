package es.uniapi.modules.model;

import java.util.Date;

import es.uniapi.modules.model.config.SHA1;

public class UserLogin {

	public UserLogin(String user, String pass, Date creationDate, String rol) {
		super();
		this.user = user;
		this.pass = pass;
		this.creationDate = creationDate;
		this.rol = rol;
	}

	private String user;
	private String pass;
	private Date creationDate;
	
	private String rol;

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	public Date getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(Date creationDate) {
		this.creationDate = creationDate;
	}

	public String getRol() {
		return rol;
	}

	public void setRol(String rol) {
		this.rol = rol;
	}

	@Override
	public String toString() {
		return "UserLogin [user=" + user + ", pass=" + pass + ", creationDate=" + creationDate + ", rol=" + rol + "]";
	}

	public String hash(){
		return SHA1.encryptPassword(this.toString());
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		UserLogin other = (UserLogin) obj;
		if (creationDate == null) {
			if (other.creationDate != null)
				return false;
		} else if (!creationDate.equals(other.creationDate))
			return false;
		if (pass == null) {
			if (other.pass != null)
				return false;
		} else if (!pass.equals(other.pass))
			return false;
		if (rol == null) {
			if (other.rol != null)
				return false;
		} else if (!rol.equals(other.rol))
			return false;
		if (user == null) {
			if (other.user != null)
				return false;
		} else if (!user.equals(other.user))
			return false;
		return true;
	}

}
